package edu;

public class Test27 {
	public static void main(String[] args) {
		//for 문
		for(int i =10; i < 10 ; i++) {
			System.out.println("for ok");
			System.out.println(i);
		}
		
		//while문
		int j = 100;
		while(j < 10);{
			System.out.println("while ok");
			System.out.println(j);
		}
		
		//do - while문
		
		int k = 10;
		do {
			System.out.println("do-while ok");
			System.out.println(k);
			k++;
		}while(k < 10);
		
	}

}
